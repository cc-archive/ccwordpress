<h4>Featured Audio Sites</h4>
<div class="post">
<p><!-- a href="http://wiki.creativecommons.org/Welcome_To_The_Podcasting_Legal_Guide" -->
	<a href="/podcasting">
	<img src="/images/podcasting/podder.jpg" alt="PLG" border="0" align="left" hspace="7" style="margin-right: 5px;"/>
	<strong>Podcasting Legal Guide</strong></a><br/>
	Inspired by the Electronic Frontier Foundation's <a href="http://www.eff.org/bloggers/lg/">Legal Guide for Bloggers</a>, the Podcasting Legal Guide is designed to outline both legal and practical issues that are specifically relevant for podcasters, such as using music and video in a podcast.

</p><br clear="all"/>

<p><a href="/legalmusicforvideos"><strong>Legal Music For Remixing</strong></a><br />
Information on finding and using CC-licensed music for remixing and sampling.
</p>

<p><a href="http://pumpaudio.com"><strong>Pump Audio</strong></a><br/>
<a href="http://pumpaudio.com">Pump Audio</a> works with independent musicians to get their music licensed in commercial media projects. You submit your work to Pump, and if they can find someone who wants to use it in their project, Pump will handle the arrangements and pay you 50% of the resulting licensing fees and/or royalties. Pump offers a variety of Creative Commons license options on their site for musicians who want to allow non-commercial use of their work.
</p>
	
<p><a href="http://www.jamendo.com/"><strong>Jamendo</strong></a><br/>
Provides an archive of CC-licensed music that's available for download via P2P. Jamendo features tags, user reviews and personalized recommendations, so you can easily find new tunes that suit your tastes. </p>

<p><a href="http://ccmixter.org/"><strong>ccMixter</strong></a><br />

A music remix site that tracks samples across different artists, all available under sampling licenses so that you too can join in the mix.</p>

<p><a href="http://magnatune.com/"><strong>Magnatune</strong></a><br />
A new music label focused on try-before-you-buy music. Features licensed, downloadable tracks from a wide variety of artists that can also be purchased direct from the site</p>

<p><a href="http://www.sectionz.com/cc.asp"><strong>SectionZ</strong></a><br />
An electronic music community that offers Creative Commons-licensed tracks from genres like IDM, drum 'n' bass, and ambient.
</p>

<p><a href="http://www.dmusic.com/"><strong>Dmusic</strong></a><br />
The oldest online digital music community, DMusic offers Creative Commons licenses for artists hosting their music.</p>

<p><a href="http://www.opsound.org/"><strong>Opsound</strong></a><br />
Features a pool of Creative Commons licensed audio. You may <a href="http://www.opsound.org/opsound/call.html" style="font-weight:normal">submit your MP3s</a></p>

<p><a href="http://www.oyez.org/oyez/resource/nitf/273/"><strong>OYEZ</strong></a>	<br />
Years of Supreme Court proceedings available as licensed MP3, with more to come</p>	

<p><a href="http://www.soundclick.com/"><strong>SoundClick</strong></a><br />

Thousands of licensed music files are <a href="http://www.soundclick.com/genres/cc_license.cfm">available here</a>, on this community music hosting service.

</p>

<p><a href="http://freesound.iua.upf.edu"><strong>Freesound</strong></a><br/>
A collaborative database of Creative Commons licensed sounds.</p>


<p><a href="http://www.diesel-u-music.com/"><strong>Diesel-U-Music</strong></a><br/>

The 2006 edition of this international music contest was open to unsigned artists playing rock, urban/hip-hop, and electronic music. This year's entries are licensed to the public under the <a href="http://creativecommons.org/licenses/by-nc/2.5/">CC Attribution-NonCommercial 2.5 license</a>. </p>
</div>

